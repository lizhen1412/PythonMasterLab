#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
示例 02：单行注释与行尾注释（#）。

要点：
1) `#` 到行末的内容都是注释（字符串/字节串里的 # 不算注释）。
2) 行尾注释通常与代码间隔至少两个空格（PEP 8 习惯）。
3) 注释仅供人/工具读取，解释器一般会忽略（编码声明除外）。
"""


def main() -> None:
    # 这是单行注释：整行都是注释

    answer = 42  # 行尾注释：说明这个值的含义
    pi = 3.14159  # 行尾注释也可以用于简短解释

    # `#` 出现在字符串里，不会开启注释
    text = "# 这不是注释，而是字符串内容"

    print("answer =", answer)
    print("pi     =", pi)
    print("text   =", text)

    # 注释可以出现在缩进块内
    if answer > 0:
        # 这里的注释只影响可读性，不影响逻辑
        print("answer 是正数")


if __name__ == "__main__":
    main()
